//JWT AUTH USER SET UP
const mongoose = require ('mongoose');
const Schema = mongoose.Schema;

//Create User Schema 
const UserSchema = ({
    name: {
        type: String,
        required: true
    },
    email: {
        type: String,
        unique: true
    },
    password: {
        type: String,
        required: true
    },
    register_date: {
        type: Date,
        default: Date.now
    }
});

module.exports = User = mongoose.model('user', UserSchema);